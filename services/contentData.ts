const generateReviews = (baseId: number, count: number, role: string, topic: string, ctaText: string, ctaLink: string): Review[] => {
    const reviews: Review[] = [];
    for (let i = 0; i < count; i++) {
        reviews.push({
            id: baseId + i,
            name: `Клиент ${baseId + i}`,
            role: role,
            text: `Отзыв про ${topic} #${i + 1}. Очень доволен результатом, все прошло гладко и профессионально. Рекомендую!`,
            fullText: `Это полный текст отзыва про ${topic}. Я обратился в агентство с задачей ${topic} и остался полностью удовлетворен качеством услуг. Менеджеры были на высоте.`,
            avatar: `https://picsum.photos/100/100?random=${baseId + i}`,
            ctaText: i % 2 === 0 ? ctaText : undefined, // Add CTA to every second review
            ctaLink: ctaLink
        });
    }
    return reviews;
};

